#include <iostream>
#include <vector>
using namespace std;

int main() {
    vector<unsigned char> flag_table = {
        0x57, 0x43, 0x47, 0x59, 0x26, 0x29, 0x25, 0x6f,
        0x5a, 0x75, 0x62, 0x62, 0x6d, 0x73, 0x7a, 0x26,
        0x62, 0x62, 0x6d, 0x73, 0x47, 0x47, 0x42, 0x73,
        0x7e, 0x7d, 0x78, 0x79, 0x35, 0x91
    };

    string result = "";

    for (int i = 0; i < flag_table.size(); ++i) {
        char c = static_cast<char>((flag_table[i] ^ 0x11) - 3);
        result += c;
    }

    cout << "Recovered input: " << result << endl;

    return 0;
}